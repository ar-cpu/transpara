class AdvancedBiasDetector:
    def __init__(self):
        self.vectorizer = None
        self.scaler = None
        self.model = None
        self.label_mapping = {
            'extreme_left': 0,
            'left': 1,
            'center_left': 2,
            'center': 3,
            'center_right': 4,
            'right': 5,
            'extreme_right': 6
        }
        self.reverse_mapping = {v: k for k, v in self.label_mapping.items()}

    def extract_linguistic_features(self, text):
        features = []

        extreme_left_markers = ['capitalist', 'bourgeois', 'proletariat', 'exploit', 'ruling class',
                                'revolution', 'socialist', 'worker', 'collective', 'abolish', 'seize',
                                'means of production', 'wage slavery', 'class struggle', 'nationalize',
                                'expropriat', 'commodity', 'alienat', 'surplus value']
        left_markers = ['universal', 'equality', 'justice', 'rights', 'protect', 'ensure',
                       'affordable', 'healthcare', 'education', 'climate', 'invest', 'community',
                       'progressive', 'equity', 'inclusion', 'access', 'support', 'program']
        center_left_markers = ['reform', 'evidence', 'pragmatic', 'balance', 'smart', 'sustainable',
                               'targeted', 'effective', 'outcome', 'partnership', 'accountability']
        center_markers = ['according', 'data', 'study', 'research', 'percent', 'report',
                         'analysis', 'indicate', 'suggests', 'both', 'various', 'mixed',
                         'methodology', 'sample', 'correlation', 'finding']
        center_right_markers = ['freedom', 'choice', 'competition', 'efficient', 'innovation',
                               'growth', 'business', 'market', 'lower', 'reduce', 'incentive',
                               'deregulat', 'privatiz', 'fiscal', 'enterprise']
        right_markers = ['liberty', 'constitution', 'traditional', 'sovereignty', 'law and order',
                        'border', 'illegal', 'overreach', 'fundamental', 'values', 'amendment',
                        'religious', 'family', 'patriot', 'conservative', 'founder']
        extreme_right_markers = ['globalist', 'elite', 'conspiracy', 'tyrann', 'invasion',
                                'traitor', 'deep state', 'infiltrat', 'cabal', 'stolen',
                                'replacement', 'regime', 'awakened', 'patriot', 'resist']

        text_lower = text.lower()

        features.append(sum(1 for w in extreme_left_markers if w in text_lower))
        features.append(sum(1 for w in left_markers if w in text_lower))
        features.append(sum(1 for w in center_left_markers if w in text_lower))
        features.append(sum(1 for w in center_markers if w in text_lower))
        features.append(sum(1 for w in center_right_markers if w in text_lower))
        features.append(sum(1 for w in right_markers if w in text_lower))
        features.append(sum(1 for w in extreme_right_markers if w in text_lower))

        exclamations = text.count('!')
        all_caps_words = len(re.findall(r'\b[A-Z]{2,}\b', text))
        features.append(exclamations)
        features.append(all_caps_words)

        sentences = re.split(r'[.!?]+', text)
        avg_sentence_length = np.mean([len(s.split()) for s in sentences if s.strip()])
        features.append(avg_sentence_length)

        questions = text.count('?')
        features.append(questions)

        absolute_words = ['must', 'always', 'never', 'all', 'every', 'completely', 'entirely', 'only']
        absolute_count = sum(1 for w in absolute_words if w in text_lower)
        features.append(absolute_count)

        first_plural = len(re.findall(r'\b(we|us|our|ours)\b', text_lower))
        features.append(first_plural)

        second_person = len(re.findall(r'\b(you|your|yours)\b', text_lower))
        features.append(second_person)

        negation = len(re.findall(r'\b(not|no|never|nothing|neither|nor|nobody)\b', text_lower))
        features.append(negation)

        comparative = len(re.findall(r'\b(more|most|less|least|better|best|worse|worst|greater|greatest)\b', text_lower))
        features.append(comparative)

        modals = len(re.findall(r'\b(should|must|would|could|might|may|will|shall)\b', text_lower))
        features.append(modals)

        numbers = len(re.findall(r'\b\d+\.?\d*%?\b', text))
        features.append(numbers)

        quotes = text.count('"') // 2
        features.append(quotes)

        return np.array(features).reshape(1, -1)

    def train(self, texts, labels):
        print(f"dataset: {len(texts)} examples")
        print(f"distribution: {Counter(labels)}")

        y = np.array([self.label_mapping[label] for label in labels])

        self.vectorizer = TfidfVectorizer(
            max_features=5000,
            ngram_range=(1, 3),
            min_df=2,
            max_df=0.8,
            sublinear_tf=True,
            strip_accents='unicode',
            lowercase=True,
            stop_words='english'
        )

        tfidf_features = self.vectorizer.fit_transform(texts)

        linguistic_features_list = [self.extract_linguistic_features(text) for text in texts]
        linguistic_features = np.vstack(linguistic_features_list)

        X_combined = sp.hstack([tfidf_features, linguistic_features])

        self.scaler = StandardScaler(with_mean=False)
        X_scaled = self.scaler.fit_transform(X_combined)

        X_train, X_test, y_train, y_test = train_test_split(
            X_scaled, y, test_size=0.3, random_state=42, stratify=y
        )

        print(f"train: {X_train.shape[0]}, test: {X_test.shape[0]}")

        param_grid = {
            'n_estimators': [700],
            'max_depth': [20, 25],
            'min_samples_split': [10],
            'min_samples_leaf': [4],
            'max_features': ['sqrt'],
            'class_weight': ['balanced_subsample'],
            'min_impurity_decrease': [0.001]
        }

        rf_base = RandomForestClassifier(random_state=42, n_jobs=-1)

        grid_search = GridSearchCV(
            rf_base,
            param_grid,
            cv=5,
            scoring='f1_weighted',
            n_jobs=-1,
            verbose=1
        )

        grid_search.fit(X_train, y_train)

        print(f"best params: {grid_search.best_params_}")
        print(f"best cv score: {grid_search.best_score_:.4f}")

        self.model = grid_search.best_estimator_

        cv_scores = cross_val_score(self.model, X_scaled, y, cv=5, scoring='f1_weighted')
        print(f"cv f1: {cv_scores.mean():.4f} (+/- {cv_scores.std() * 2:.4f})")

        self.model.fit(X_scaled, y)

        y_pred = self.model.predict(X_test)

        print(f"\ntest accuracy: {accuracy_score(y_test, y_pred):.4f}")
        print(classification_report(
            y_test, y_pred,
            target_names=list(self.label_mapping.keys()),
            digits=4
        ))

    def predict(self, text):
        tfidf_features = self.vectorizer.transform([text])
        linguistic_features = self.extract_linguistic_features(text)
        combined = sp.hstack([tfidf_features, linguistic_features])
        scaled = self.scaler.transform(combined)

        prediction = self.model.predict(scaled)[0]
        probabilities = self.model.predict_proba(scaled)[0]

        return self.reverse_mapping[prediction], probabilities

    def save(self, filepath):
        model_data = {
            'model': self.model,
            'tfidf_vectorizer': self.vectorizer,
            'scaler': self.scaler,
            'label_mapping': self.label_mapping,
            'reverse_mapping': self.reverse_mapping
        }
        joblib.dump(model_data, filepath)
        print(f"saved: {filepath}")


